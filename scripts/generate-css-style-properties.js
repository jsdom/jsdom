"use strict";

const fs = require("node:fs/promises");
const path = require("node:path");
const { definitionSyntax } = require("css-tree");

const outputDir = path.resolve(__dirname, "../../lib/generated");
const styleDeclarationDir = "../jsdom/living/cssom/style-declaration";

const caseSensitiveTypes = ["custom-ident", "dashed-ident", "string"];
const dimensionTypes = [
  "angle",
  "angle-percentage",
  "flex",
  "frequency",
  "frequency-percentage",
  "integer",
  "length",
  "length-percentage",
  "number",
  "percentage",
  "ratio",
  "time",
  "time-percentage"
];
const functionTypes = ["color", "image", "paint"];
const typeList = new Set([...caseSensitiveTypes, ...dimensionTypes, ...functionTypes]);

main();

async function main() {
  const cssModule = await import("@webref/css");
  const css = cssModule.default;

  const propertiesDir = path.resolve(__dirname, "../../lib/jsdom/living/cssom/style-declaration/properties");
  const propertyFiles = await fs.readdir(propertiesDir);
  const implementedProperties = new Map(propertyFiles.map(file => [file.replace(/\.js$/, ""), file]));

  const [dateToday] = new Date().toISOString().split("T");
  const fileHeader = `"use strict";\n// autogenerated - ${dateToday}\n`;

  const { properties, types } = await css.listAll();
  const definitions = new Map(
    properties.map(definition => {
      delete definition.extended;
      return [definition.name, definition];
    })
  );
  const syntaxes = new Map(types.map(definition => [`<${definition.name}>`, definition]));

  await Promise.all([
    generateDefinitions({ definitions, fileHeader }),
    generateDescriptors({ definitions, fileHeader, implementedProperties, syntaxes })
  ]);
}

function generateDefinitions({ definitions, fileHeader }) {
  const output = `${fileHeader}\nmodule.exports = new Map(${JSON.stringify([...definitions], null, 2)});\n`;
  return fs.writeFile(path.resolve(outputDir, "css-property-definitions.js"), output);
}

function generateDescriptors({ definitions, fileHeader, implementedProperties, syntaxes }) {
  const requires = [];
  const descriptors = [];

  for (const [canonicalProperty, { legacyAliasOf, styleDeclaration, syntax }] of definitions) {
    const camelizedProperty = dashedToCamelCase(canonicalProperty);
    const camelizedAliasProperty = legacyAliasOf ? dashedToCamelCase(legacyAliasOf) : "";

    let descriptorSource;
    if (implementedProperties.has(camelizedProperty)) {
      const fileName = implementedProperties.get(camelizedProperty);
      requires.push(`const ${camelizedProperty} = require("${styleDeclarationDir}/properties/${fileName}");`);
      descriptorSource = `${camelizedProperty}.descriptor`;
    } else if (implementedProperties.has(camelizedAliasProperty)) {
      descriptorSource = `${camelizedAliasProperty}.descriptor`;
    } else {
      const opts = JSON.stringify(createDescriptorOpts({ syntax, definitions, syntaxes }));
      descriptorSource = `createGenericPropertyDescriptor("${canonicalProperty}", ${opts})`;
    }

    for (const property of styleDeclaration) {
      descriptors.push(`"${property}": ${descriptorSource}`);
    }
  }

  const output = `${fileHeader}
${requires.sort().join("\n")}
const { createGenericPropertyDescriptor } = require("${styleDeclarationDir}/property-descriptors.js");

module.exports = {
  ${descriptors.join(",\n  ")}
};
`;

  return fs.writeFile(path.resolve(outputDir, "css-property-descriptors.js"), output);
}

function createDescriptorOpts({ syntax, definitions, syntaxes }) {
  const opts = {
    caseSensitive: false
  };
  const collectedTypes = collectTypes({ syntax, definitions, syntaxes });
  if (!collectedTypes.size) {
    return opts;
  }

  for (const type of caseSensitiveTypes) {
    if (collectedTypes.has(type)) {
      opts.caseSensitive = true;
      break;
    }
  }

  const dimensions = {};
  for (const key of dimensionTypes) {
    if (!collectedTypes.has(key)) {
      continue;
    }

    const { opts: keyOpts } = collectedTypes.get(key);
    const opt = {};
    if (keyOpts) {
      const { max, min } = keyOpts;
      if (max !== undefined) {
        opt.max = max;
      }
      if (min !== undefined) {
        opt.min = min;
      }
    }

    let type;
    switch (key) {
      case "angle":
      case "angle-percentage": {
        type = "angle";
        break;
      }
      case "integer":
      case "number": {
        type = "number";
        break;
      }
      case "length":
      case "length-percentage": {
        type = "length";
        break;
      }
      case "percentage": {
        type = "percentage";
        break;
      }
      default: {
        type = "dimension";
      }
    }

    opt.type = type;
    dimensions[type] = opt;
  }

  if (Object.keys(dimensions).length) {
    opts.dimensionTypes = dimensions;
  }

  const functions = {};
  for (const type of functionTypes) {
    if (collectedTypes.has(type)) {
      functions[type] = type;
    }
  }

  if (Object.keys(functions).length) {
    opts.functionTypes = functions;
  }

  return opts;
}

function collectTypes({ syntax, definitions, syntaxes }) {
  const collectedTypes = new Map();
  if (!syntax) {
    return collectedTypes;
  }

  const ast = definitionSyntax.parse(syntax);
  if (ast.type !== "Group") {
    return collectedTypes;
  }

  const subTypeList = new Set();
  const propertyList = new Set();

  definitionSyntax.walk(ast, {
    enter(node) {
      const { name, type } = node;
      if (type === "Type") {
        if (typeList.has(name)) {
          collectedTypes.set(name, node);
        } else if (!name.endsWith("()")) {
          subTypeList.add(name);
        }
      } else if (type === "Property") {
        propertyList.add(name);
      }
    }
  });

  for (const type of subTypeList) {
    const typeKey = `<${type}>`;
    if (!syntaxes.has(typeKey)) {
      continue;
    }

    const { syntax: typeSyntax } = syntaxes.get(typeKey);
    const expandedList = collectTypes({ syntax: typeSyntax, definitions, syntaxes });
    for (const [key, value] of expandedList) {
      collectedTypes.set(key, value);
    }
  }

  for (const property of propertyList) {
    if (!definitions.has(property)) {
      continue;
    }

    const { syntax: propertySyntax } = definitions.get(property);
    const expandedList = collectTypes({ syntax: propertySyntax, definitions, syntaxes });
    for (const [key, value] of expandedList) {
      collectedTypes.set(key, value);
    }
  }

  return collectedTypes;
}

function dashedToCamelCase(dashed) {
  if (dashed.startsWith("--")) {
    return dashed;
  }

  return dashed.replace(/^-(webkit)/, "$1").replace(/-([a-z])/g, (_, ch) => ch.toUpperCase());
}
